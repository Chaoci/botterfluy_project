                      <li data-aos="fade-up">
                        <h3>心情、情緒</h3>
                        <p>有了寶寶，生活中不再只有自己，是甜蜜也是沈重的負荷，你的理想、你的生涯規劃也都因此需要調整，也許轉個彎，也許延長路程，甚至必須改道，必須思考清楚，該如何抉擇才能兼顧你與孩子的未來。</p>
